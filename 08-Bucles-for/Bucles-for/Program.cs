// Bucle while

Console.WriteLine("Ejemplo while");
int num = 1;
while (num <= 10)
{
    Console.WriteLine(num);
    num++;
}

// Bucle for

Console.WriteLine("Ejemplo for");
for (int nn = 1; nn <= 10; nn++)
{
    Console.WriteLine(nn);
}

